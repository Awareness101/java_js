package handwrittencode;

public class MaximumAndMinimumValuesOfConstants {
    public static void main(String[] args) {
        System.out.println("Maximum value of int:\s"+Integer.MAX_VALUE);
        System.out.println("Minimum value of int:\s"+Integer.MIN_VALUE);
        System.out.println("Maximum value of byte:\s"+Byte.MAX_VALUE);
        System.out.println("Minimum value of byte:\s"+Byte.MIN_VALUE);
        System.out.println("Maximum value of long:\s"+Long.MAX_VALUE);
        System.out.println("Minimum value of long:\s"+Long.MIN_VALUE);
        System.out.println("Maximum value of float:\s"+Float.MAX_VALUE);
        System.out.println("Minimum value of float:\s"+Float.MIN_VALUE);
    }
}
